// turn data array into object with each country as key and 
// all of its medals data as value
export function convertData(data) {
    const dataObj = {};
    data = data.post;
    data.forEach(row => {
        const [country, medal, year, sport, sportEvent, athlete, source] = row;

        if (!dataObj[country]) {
            dataObj[country] = []
        }

        dataObj[country].push({
            medal,
            year,
            sport,
            sportEvent,
            athlete,
            source
        });
    });
    return dataObj;
}

